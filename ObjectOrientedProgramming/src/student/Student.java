package student;

public class Student {
	String name;
	int age;
	int korean;
	int english;
	int math;
	
	public void setName(String n) {
		name = n;
	};
	public void setAge(int a) {
		age = a;
	};
	public void setKor(int kor) {
		korean = kor;
	};
	public void setEng(int eng) {
		english = eng;
	};
	public void setMath(int mat) {
		math = math;
	};
}
